using System;

namespace redb.Core.Models.Contracts
{
    /// <summary>
    /// Интерфейс пользователя REDB
    /// Представляет пользователя системы с его основными свойствами
    /// </summary>
    public interface IRedbUser
    {
        /// <summary>
        /// Уникальный идентификатор пользователя
        /// </summary>
        long Id { get; }
        
        /// <summary>
        /// Логин пользователя (уникальный)
        /// </summary>
        string Login { get; }
        
        /// <summary>
        /// Имя пользователя
        /// </summary>
        string Name { get; }
        
        /// <summary>
        /// Пароль пользователя (хешированный)
        /// </summary>
        string Password { get; }
        
        /// <summary>
        /// Активен ли пользователь
        /// </summary>
        bool Enabled { get; }
        
        /// <summary>
        /// Дата регистрации пользователя
        /// </summary>
        DateTime DateRegister { get; }
        
        /// <summary>
        /// Дата увольнения (если null - пользователь активен)
        /// </summary>
        DateTime? DateDismiss { get; }
        
        /// <summary>
        /// Телефон пользователя (опционально)
        /// </summary>
        string? Phone { get; }
        
        /// <summary>
        /// Email пользователя (опционально)
        /// </summary>
        string? Email { get; }
        
        /// <summary>
        /// Дополнительный ключ пользователя (опционально)
        /// Может использоваться для внешних интеграций или дополнительной идентификации
        /// </summary>
        long? Key { get; }
        
        /// <summary>
        /// Целочисленный код пользователя (опционально)
        /// Может использоваться для категоризации, групп доступа или внешних систем
        /// </summary>
        long? CodeInt { get; }
        
        /// <summary>
        /// Строковый код пользователя (опционально)
        /// Может использоваться для кодов подразделений, филиалов или специальных меток
        /// </summary>
        string? CodeString { get; }
        
        /// <summary>
        /// GUID код пользователя (опционально)  
        /// Может использоваться для уникальной идентификации в распределенных системах
        /// </summary>
        Guid? CodeGuid { get; }
        
        /// <summary>
        /// Заметка или комментарий к пользователю (опционально)
        /// Может содержать дополнительную информацию, инструкции или примечания
        /// </summary>
        string? Note { get; }
        
        /// <summary>
        /// Хеш пользователя для проверки целостности данных (опционально)
        /// Генерируется автоматически на основе основных свойств пользователя
        /// </summary>
        Guid? Hash { get; }
    }
}
